#Solicita ao usuario para digitar a variação de espaçõ e a variação de tempo
delta_s = float(input("Digite a variação de espaco:"))
delta_t = float(input("Digite a variação de tempo:"))

#Calcula a velocidade média usando a fórmula velocidade = delta_s/delta_t
velocidade_media = delta_s / delta_t

#Exibe o resultado da velocidade média
print("A velocidade média é:", velocidade_media)
